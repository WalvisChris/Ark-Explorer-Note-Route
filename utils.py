import math
from note import Note

def get_distance(a: tuple, b: tuple) -> float:
    x1, y1 = a
    x2, y2 = b
    return math.sqrt((x2 - x1)**2 + (y2 - y1)**2)

def get_total_distance(path):
    total = 0
    for i in range(len(path) - 1):
        total += get_distance(path[i], path[i + 1])
    return total

